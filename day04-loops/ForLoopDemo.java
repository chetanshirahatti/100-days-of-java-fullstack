public class ForLoopDemo {

    public static void main(String[] args) {

        /*
         Loop is used when we want to repeat some work again and again.
         For loop is used when we already know
         how many times the loop should run.
        */

        // This loop will run 5 times
        for (int i = 1; i <= 5; i++) {

            // i value increases by 1 each time
            System.out.println("Count: " + i);
        }
    }
}
